// Function to get the imported image from a path
export const getImage = (path) => {
  return imageMap[path] || path; // Return the imported image or the original path if not found
};

// Function to transform product data with imported images
export const transformProductData = (product) => {
  if (!product) return product;

  const transformed = { ...product };

  // Transform main image
  if (transformed.image) {
    transformed.image = {
      mobile: getImage(transformed.image.mobile),
      tablet: getImage(transformed.image.tablet),
      desktop: getImage(transformed.image.desktop),
    };
  }

  // Transform category image
  if (transformed.categoryImage) {
    transformed.categoryImage = {
      mobile: getImage(transformed.categoryImage.mobile),
      tablet: getImage(transformed.categoryImage.tablet),
      desktop: getImage(transformed.categoryImage.desktop),
    };
  }

  // Transform gallery images
  if (transformed.gallery) {
    transformed.gallery = {
      first: {
        mobile: getImage(transformed.gallery.first.mobile),
        tablet: getImage(transformed.gallery.first.tablet),
        desktop: getImage(transformed.gallery.first.desktop),
      },
      second: {
        mobile: getImage(transformed.gallery.second.mobile),
        tablet: getImage(transformed.gallery.second.tablet),
        desktop: getImage(transformed.gallery.second.desktop),
      },
      third: {
        mobile: getImage(transformed.gallery.third.mobile),
        tablet: getImage(transformed.gallery.third.tablet),
        desktop: getImage(transformed.gallery.third.desktop),
      },
    };
  }

  // Transform others images
  if (transformed.others) {
    transformed.others = transformed.others.map(other => ({
      ...other,
      image: {
        mobile: getImage(other.image.mobile),
        tablet: getImage(other.image.tablet),
        desktop: getImage(other.image.desktop),
      },
    }));
  }

  return transformed;
};

